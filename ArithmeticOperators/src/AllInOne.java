
public class AllInOne {

	public static void main(String[] args) {
		       
	        int num1 = 10, num2 = 20; 
	 
	        System.out.println("Substraction = " + (num2 - num1)); 
	        System.out.println("Add = " + (num2 + num1));
	        System.out.println("Mul = " + (num2 * num1));
	        System.out.println("Div = " + (num2 / num1));
	        System.out.println("Mod = " + (num2 % num1));
	        
	        System.out.println("Post Incremenet = " + num1++);
	        System.out.println("Post Incremenet = " + num1);
	        
	        System.out.println("Pre Incremenet = " + (++num1));
	        System.out.println("Pre Incremenet = " + num1);
	       
	}

}
