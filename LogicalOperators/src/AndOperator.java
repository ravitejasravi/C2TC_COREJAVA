
public class AndOperator {

	public static void main(String[] args) {
		
        int a = 10, b = 20; 
       
        System.out.println("Conditions 1");
        if ((a == 10) && (b == 20)) { 
            System.out.println("Conditions 1 True"); 
        } 
        else
            System.out.println("False conditions"); 
        
        System.out.println("Conditions 2");
        if ((a == 20) && (b == 10)) { 
            System.out.println("Conditions 1 True"); 
        } 
        else
            System.out.println("False conditions"); 
        
  
	}

}
